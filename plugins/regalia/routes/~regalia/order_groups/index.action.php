<h1>Regalia order groups</h1>
<p>
    A regalia order group defines a collection of regalia orders which will be received and processed together by the bookstore.
    Generally there will be one set of regalia order groups per semester, and they will be split into platform members and normal orders.
    Normal orders may be further split into groups for main/north campus, or orders with/without tams, etc.
</p>